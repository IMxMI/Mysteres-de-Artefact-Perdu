package org.jeu;

/**
 * Enumère les directions possibles de sortie d'une zone dans le jeu.
 * <p>
 * Chaque valeur correspond à une direction cardinale.
 * </p>
 */
public enum Sortie {
	/** Sortie vers le nord */
	NORD,
	/** Sortie vers le sud */
	SUD,
	/** Sortie vers l'est */
	EST,
	/** Sortie vers l'ouest */
	OUEST;
}
